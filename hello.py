"""This finds the product of two numbers"""


def product(x, y):
    """Function to multiply two numbers, x and y"""
    return x * y


def main():
    """This is the main function call"""
    x = 3
    y = 4
    print(f"This is the product: {x} x {y} = {product(x,y)}")


if __name__ == "__main__":
    main()
